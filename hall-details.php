    $(document).ready(function () {
        // The slider being synced must be initialized first
        $('#carousel').flexslider({
            animation: "slide",
            controlNav: false,
            animationLoop: false,
            slideshow: false,
            itemWidth: 210,
            itemMargin: 5,
            asNavFor: '#slider'
        });

        $('#slider').flexslider({
            animation: "slide",
            controlNav: false,
            animationLoop: false,
            slideshow: false,
            sync: "#carousel",
            before: function () {
                $('video').each(function () {
                    $(this).get(0).load();
                });
            },
            after: function () {
                // sets active_id to the active slide
                var active_id = $('.flex-active-slide').attr('id');

                //$('video').trigger('pause');

                //if the active slide is the video slide...
                if (active_id == "slide1") {
                    //play the video and pause the slider
                    myVideo1.play();
                    $('.flexslider').flexslider("pause");
                    //when the video has ended, go to the next slide and play the slider
                    myVideo1.onended = function () {
                        $('.flexslider').flexslider("next");
                        $('.flexslider').flexslider("play");
                    }
                }
            },
        });
    });
</script>
